/**
 * Pick a source word for the given 1-indexed round. Round 1 lands
 * in tier 0; subsequent rounds climb a tier until we run out and
 * clamp at the longest pool. The pool itself is randomized per draw
 * so the same tier doesn't repeat the same word across two rounds
 * of one game.
 */
export function pickSourceWord(roundNumber: number): string {
  const tierIdx = Math.min(roundNumber - 1, SOURCE_WORDS_BY_TIER.length - 1);
  const pool = SOURCE_WORDS_BY_TIER[Math.max(0, tierIdx)] ?? SOURCE_WORDS_BY_TIER[0]!;
  return pool[Math.floor(Math.random() * pool.length)]!;
}

